#!/usr/bin/env node

"use strict";

const fs = require("node:fs");
const path = require("node:path");

const ROOT = path.resolve(__dirname, "..");
const SOURCE = path.join(ROOT, "governance", "document-registry.json");
const OUTPUTS = {
  markdown: path.join(ROOT, "governance", "document-registry.md"),
  agent: path.join(ROOT, "documents", "agent-md", "document-registry.md"),
  html: path.join(ROOT, "documents", "document-registry.html"),
};
const CHECK_ONLY = process.argv.includes("--check");

function escapeHtml(value) {
  return String(value)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#39;");
}

function code(value) {
  return value === null || value === undefined ? "Not recorded" : `\`${value}\``;
}

function paths(entry) {
  return [entry.currentSource, ...entry.artifacts].filter(Boolean);
}

function renderMarkdown(registry, agent = false) {
  const sourceNote = agent
    ? "> Generated agent view. Edit `governance/document-registry.json`, then run `node scripts/generate-document-registry.js`."
    : "> Generated human-readable view. The machine-readable source is `document-registry.json`; do not edit this file directly.";
  const rows = registry.entries.map((entry) => {
    const files = paths(entry).map(code).join("; ");
    return `| ${entry.id} | ${entry.title} | ${entry.status} | ${entry.approval} | ${entry.owner} | ${entry.artifactClass} | ${files} |`;
  }).join("\n");
  const reviews = registry.entries.filter((entry) => entry.approval === "pending").map((entry) => `- [ ] **${entry.id} — ${entry.title}:** ${entry.reviewers.join(", ")}.`).join("\n");
  const migrations = registry.entries.filter((entry) => entry.artifactClass.startsWith("legacy-")).map((entry) => `- [ ] **${entry.id}:** migrate in ${entry.migrationPhase}. ${entry.notes}`).join("\n");

  return `# Work2Cash Document Registry

${sourceNote}

## Registry control

| Field | Value |
| --- | --- |
| Schema version | ${registry.schemaVersion} |
| Updated | ${registry.updated} |
| Owner | ${registry.owner} |
| Lifecycle policy | ${code(registry.lifecyclePolicy)} |

## How to read this registry

- **Status** describes lifecycle; publication does not equal approval.
- **Approval** records formal reviewer acceptance.
- **Canonical source** is edited directly and may generate several outputs.
- **Legacy transitional source** is a temporary manually maintained source awaiting migration.
- **Legacy derived frozen** is non-canonical and must not be edited for normal work.
- Check an entry's detailed record below before using it as implementation authority.

## At-a-glance register

| ID | Document | Status | Approval | Owner | Artifact class | Source and artifacts |
| --- | --- | --- | --- | --- | --- | --- |
${rows}

## Detailed records

${registry.entries.map((entry) => `### ${entry.id} — ${entry.title}

| Field | Value |
| --- | --- |
| Type | ${entry.type} |
| Audience | ${entry.audience.join(", ")} |
| Owner | ${entry.owner} |
| Required reviewers | ${entry.reviewers.join(", ")} |
| Version | ${entry.version} |
| Lifecycle | ${entry.status} |
| Approval | ${entry.approval} |
| Publication | ${entry.publication} |
| Last reviewed | ${entry.lastReviewed || "Not recorded"} |
| Next review | ${entry.nextReview || "Not scheduled"} |
| Authority | ${entry.authority} |
| Artifact class | ${entry.artifactClass} |
| Current source | ${code(entry.currentSource)} |
| Other artifacts | ${entry.artifacts.length ? entry.artifacts.map(code).join(", ") : "None"} |
| Migration phase | ${entry.migrationPhase} |
| Supersedes | ${entry.supersedes || "None"} |
| Tags | ${entry.tags.join(", ")} |

${entry.notes}
`).join("\n")}

## Pending approvals

${reviews || "No pending approvals."}

## Legacy migration queue

${migrations || "No legacy documents remain."}
`;
}

function renderHtml(registry) {
  const active = registry.entries.filter((entry) => entry.status === "active").length;
  const pending = registry.entries.filter((entry) => entry.approval === "pending").length;
  const legacy = registry.entries.filter((entry) => entry.artifactClass.startsWith("legacy-")).length;
  const rows = registry.entries.map((entry) => {
    const source = entry.currentSource
      ? `<a href="../${escapeHtml(entry.currentSource)}">${escapeHtml(entry.currentSource)}</a>`
      : "Not recorded";
    const artifacts = entry.artifacts.length
      ? `<ul>${entry.artifacts.map((artifact) => `<li><a href="../${escapeHtml(artifact)}">${escapeHtml(artifact)}</a></li>`).join("")}</ul>`
      : "None";
    return `<tr id="${escapeHtml(entry.id.toLowerCase())}">
      <td><strong>${escapeHtml(entry.id)}</strong><br><span class="registry-type">${escapeHtml(entry.type)}</span></td>
      <td><strong>${escapeHtml(entry.title)}</strong><p>${escapeHtml(entry.notes)}</p></td>
      <td><span class="status status-${escapeHtml(entry.status)}">${escapeHtml(entry.status)}</span><br>${escapeHtml(entry.approval)} approval<br>${escapeHtml(entry.publication)}</td>
      <td>${escapeHtml(entry.owner)}<br><small>Reviewers: ${escapeHtml(entry.reviewers.join(", "))}</small></td>
      <td>${escapeHtml(entry.artifactClass)}<br><small>${escapeHtml(entry.authority)}</small></td>
      <td><strong>Source</strong><br>${source}<br><strong>Artifacts</strong>${artifacts}</td>
      <td>${entry.lastReviewed ? escapeHtml(entry.lastReviewed) : "Not recorded"}<br><small>Next: ${entry.nextReview ? escapeHtml(entry.nextReview) : "Not scheduled"}</small></td>
    </tr>`;
  }).join("\n");

  return `<!doctype html>
<html lang="en">
<head>
  <meta charset="utf-8">
  <meta name="viewport" content="width=device-width, initial-scale=1">
  <meta name="description" content="Work2Cash document ownership, lifecycle, approval, authority and artifact registry.">
  <title>Document Registry | Work2Cash Docs</title>
  <link rel="stylesheet" href="../assets/css/pilot-docs.css">
  <script src="../assets/js/guard.js"></script>
</head>
<body>
  <a class="skip-link" href="#content">Skip to registry</a>
  <header class="site-header"><a class="brand" href="../index.html">Work2Cash Docs</a><nav aria-label="Document navigation"><a href="../index.html">Portal home</a><a href="./agent-md/document-registry.md" download>Download agent Markdown</a></nav></header>
  <main id="content" class="index-shell" tabindex="-1">
    <header class="index-hero"><div class="eyebrow">Governance · Phase 0</div><h1>Document Registry</h1><p>Check ownership, lifecycle, approval and authority before relying on a document. Published does not necessarily mean approved.</p></header>
    <section class="notice" aria-labelledby="registry-warning"><h2 id="registry-warning">Current authority warning</h2><p>The Main Enterprise Architecture v1 is the designated provisional baseline, but formal approval is pending. Material cross-document conflicts must be resolved before implementation.</p></section>
    <section class="registry-summary" aria-label="Registry summary"><div><strong>${registry.entries.length}</strong><span>registered families</span></div><div><strong>${active}</strong><span>active</span></div><div><strong>${pending}</strong><span>pending approval</span></div><div><strong>${legacy}</strong><span>legacy families</span></div></section>
    <section class="document-content registry-content" aria-labelledby="register-title">
      <p class="generated-note"><strong>Generated registry:</strong> Edit <code>governance/document-registry.json</code>, not this page.</p>
      <h2 id="register-title">Registered documents</h2>
      <div class="table-wrap" tabindex="0"><table class="registry-table"><thead><tr><th scope="col">ID and type</th><th scope="col">Document</th><th scope="col">Lifecycle</th><th scope="col">Ownership</th><th scope="col">Authority and class</th><th scope="col">Files</th><th scope="col">Review</th></tr></thead><tbody>${rows}</tbody></table></div>
    </section>
  </main>
  <footer>Work2Cash documentation governance · Updated ${escapeHtml(registry.updated)}</footer>
</body>
</html>
`;
}

function expectedOutputs(registry) {
  return new Map([
    [OUTPUTS.markdown, renderMarkdown(registry, false)],
    [OUTPUTS.agent, renderMarkdown(registry, true)],
    [OUTPUTS.html, renderHtml(registry)],
  ]);
}

function run() {
  const registry = JSON.parse(fs.readFileSync(SOURCE, "utf8"));
  const expected = expectedOutputs(registry);
  const problems = [];

  if (CHECK_ONLY) {
    for (const [file, contents] of expected) {
      if (!fs.existsSync(file)) problems.push(`missing: ${path.relative(ROOT, file)}`);
      else if (fs.readFileSync(file, "utf8") !== contents) problems.push(`stale: ${path.relative(ROOT, file)}`);
    }
    if (problems.length) {
      console.error("Document registry generation check failed:\n" + problems.map((problem) => `- ${problem}`).join("\n"));
      process.exitCode = 1;
      return;
    }
    console.log(`Document registry is current: ${expected.size} generated files checked.`);
    return;
  }

  for (const [file, contents] of expected) {
    fs.mkdirSync(path.dirname(file), { recursive: true });
    fs.writeFileSync(file, contents);
  }
  console.log(`Generated ${expected.size} registry views from governance/document-registry.json.`);
}

try {
  run();
} catch (error) {
  console.error(`Document registry generation failed: ${error.message}`);
  process.exitCode = 1;
}
